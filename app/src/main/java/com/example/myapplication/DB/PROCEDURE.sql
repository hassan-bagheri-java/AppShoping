DROP PROCEDURE IF EXISTS `login`;


CREATE PROCEDURE `login`(IN email VARCHAR(200) , IN pass VARCHAR(200))
BEGIN
  INSERT INTO `users` (`email`, `password`) VALUES (email, pass)
	;
END;

CALL login('hassan4@gmail.com','123456')


